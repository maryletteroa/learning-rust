//Vector demo - print element by element
fn main() {
    //Declare integer vector
    let num_vector = vec![34, -65, 77, 12, -23, 85];
    //print value at each index
    println!("\nElement at index 0: {}", num_vector[0]);
    println!("\nElement at index 1: {}", num_vector[1]);
    println!("\nElement at index 2: {}", num_vector[2]);
    println!("\nElement at index 3: {}", num_vector[3]);
    println!("\nElement at index 4: {}", num_vector[4]);
    println!("\nElement at index 5: {}", num_vector[5]);
}